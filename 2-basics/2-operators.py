#Existen muchos operadores básicos,

#(+) Suma
2 + 2  # 4

#(-) Resta
4 - 2 # 2

# (asterisco) Multiplicación
2 * 3 # 6

# (/) División
5 / 4 # 1.25

# (//) División de enteros: redondea el valor
5 // 4 # 1

# (%) Operador de módulo: Sobrante en una división
9 % 2 # 1

# (doble asterisco) Potencias
2 ** 3 # 8

# (>) Mayor que
5 > 3 # TRUE

# (<) Menor que
1 < 10 # TRUE

# (==) Igual
1 == 5 # False

# (>=) Mayor igual
1 >= 15 # FALSE

# (<=) Menor igual
10 <= 15 # TRUE